
public class variables {

	public static void main(String[] args) {
		
		
//		Integer Stores whole numbers from -2,147,483,648 to 2,147,483,647 . 
//      Integer has 4 bytes.		
		int  num = 25454678;
//      short Stores whole numbers from -32,768 to 32,767
//      short has 2 bytes .		
		short snum = 20;
//      long Stores whole numbers from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
//		long has 8 bytes
		long lnum= 45005448;
//      Double Stores fractional numbers. Sufficient for storing 15 decimal digits
//		Double has 8 bytes
		double dnum =5.23472385437 ;
//      float Stores fractional numbers. Sufficient for storing 6 to 7 decimal digits
//	    float has 4 bytes	
		float fnum = 5.2343446f;
//		character Stores a single character/letter or ASCII values 
//		character has 2 bytes
		char ch = 'b';
//      booleans Stores true or false values
//		booleans has 1 bytes	
//      boolean use single bracket ''		
		boolean bvalue =true;
//      bytes Stores whole numbers from -128 to 127
//		bytes has 1 byte
		byte bnum= 121;
		 
		
		 System.out.println("========VARIABLES========");
		
		System.out.println("Integer :"+num);
		System.out.println("Short :"+snum);
		System.out.println("Long :"+lnum);
		System.out.println("Double :"+dnum);
		System.out.println("Float :"+fnum);
		System.out.println("Character :"+ch);
		System.out.println("Boolean :"+bvalue);
		System.out.println("Byte :"+bnum);
		
		

	}

}
